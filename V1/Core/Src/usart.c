/* 串口中断的接受回调函数 */
//uint8_t A=0xAA;
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	if(huart->Instance == USART1)		//判断串口
	{
//		HAL_UART_Transmit_DMA(&huart1,Udata.TX,10);
		if(Udata.RX[0]==0xA0)	//判断帧头
		{
			if(Udata.RX[1]==0x01)	//表示修改PID参数以及目标值
			{
				if(Udata.RX[2]==0x11)	//表示修改Kp
				{
					if(Udata.RX[3]==0x02)
					{
						car.A.PID.Kp+=0.02;
						car.B.PID.Kp+=0.02;
					}
					else 
					{
						car.A.PID.Kp-=0.02;
						car.B.PID.Kp-=0.02;
					}
				}
				else if(Udata.RX[2]==0x12)	//表示修改Ki
				{
					if(Udata.RX[3]==0x02)
					{
						car.A.PID.Ki+=0.01;
						car.B.PID.Ki+=0.01;
					}
					else 
					{
						car.A.PID.Ki-=0.01;
						car.B.PID.Ki-=0.01;
					}
				}
				else if(Udata.RX[2]==0x13)	//表示修改Kd
				{
					if(Udata.RX[3]==0x02)
					{
						car.A.PID.Kd+=0.01;
						car.B.PID.Kd+=0.01;
					}
					else 
					{
						car.A.PID.Kd-=0.01;
						car.B.PID.Kd-=0.01;
					}
				}
			}
			else if(Udata.RX[1]==0x02)	//表示修改方向
			{
				if(Udata.RX[2]==0x01)	//表示直行
				{
					car.A.PID.target_val=70;
					car.B.PID.target_val=70;
				}
				else if(Udata.RX[2]==0x02)	//表示右转
				{
					car.A.PID.target_val=70+50;
					car.B.PID.target_val=70-50;
				}
				else if(Udata.RX[2]==0x03)	//表示左转
				{
					car.A.PID.target_val=70-50;
					car.B.PID.target_val=70+50;
				}
			}
		}
		HAL_UART_Receive_DMA(&huart1,Udata.RX,4);//串口接收数据,注意：如果要在串口接受中断使用发送HAL_UART_Transmit（），Usart_buf必须是数组，不然会接受到的数据会覆盖发送的内容
	}
}
